import { getSession, signIn } from "next-auth/react";

export const getServerSideProps: GetServerSideProps = async ({ req, res }) => {
  const session = await getSession({ req });

  if (session) {
    return {
      redirect: {
        destination: "/app",
        permanent: false
      }
    };
  }

  return {
    props: {}
  };
};


